#include <iostream>
using namespace std;

int main() {
    cout << "Size of an int:      " << sizeof(int) << " bytes" << endl;
    cout << "Size of an int*:     " << sizeof(int*) << " bytes" << endl;
    cout << "Size of a double*:   " << sizeof(double*) << " bytes" << endl;
    cout << "Size of a char*:     " << sizeof(char*) << " bytes" << endl;
    return 0;
}
